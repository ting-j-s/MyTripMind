# ==================== 爬虫函数 ====================

def query_osm(query):
    """
    发送Overpass查询
    """
    try:
        response = requests.post(
            OVERPASS_URL,
            data={"data": query},
            timeout=120
        )
        data = response.json()
        return data.get("elements", [])
    except Exception as e:
        print(f"查询失败: {e}")
        return []


def elements_to_attractions(elements):
    """
    将OSM元素转换为景点格式
    """
    attractions = []
    seen_ids = set()

    for elem in elements:
        elem_type = elem.get("type")

        # 获取中心点
        if elem_type == "node":
            lat = elem.get("lat", 0)
            lon = elem.get("lon", 0)
        elif elem_type == "way" and "center" in elem:
            lat = elem["center"].get("lat", 0)
            lon = elem["center"].get("lon", 0)
        else:
            continue

        # 获取名称
        tags = elem.get("tags", {})
        name = tags.get("name", "") or tags.get("name:en", "")
        if not name:
            continue

        # 过滤掉国外地名
        if any(c in name for c in ["church", "park", "museum", "temple"]):
            if not is_chinese_name(name):
                name = tags.get("name:zh", "") or name

        elem_id = elem.get("id")
        if elem_id in seen_ids:
            continue
        seen_ids.add(elem_id)

        # 获取类型标签
        poi_type = tags.get("tourism", "") or tags.get("amenity", "") or tags.get("leisure", "") or "景点"

        attractions.append({
            "id": f"ATTR_OSM_{elem_id}",
            "name": name,
            "type": poi_type,
            "campus_id": "",
            "x": lon,
            "y": lat,
            "heat": 100,
            "rating": 4.0,
            "tags": [poi_type],
            "description": tags.get("description", ""),
            "image_url": ""
        })

    return attractions


def is_chinese_name(name):
    """判断是否是中文名"""
    return bool(re.search(r'[\u4e00-\u9fff]', str(name)))


def search_osm_poi(keyword, bbox=BEIJING_BBOX):
    """
    搜索OSM POI

    参数:
        keyword: 英文关键词
        bbox: 边界框
    """
    print(f"  搜索关键词: {keyword}")

    query = build_overpass_query(keyword, bbox)
    elements = query_osm(query)

    if not elements:
        print(f"    无结果")
        return []

    print(f"    找到 {len(elements)} 个元素")
    attractions = elements_to_attractions(elements)
    return attractions


def crawl_beijing():
    """
    爬取北京景点数据
    """
    print("=" * 50)
    print("开始从OpenStreetMap爬取北京景点数据...")
    print("=" * 50)

    all_attractions = []
    seen_names = set()

    # 英文关键词列表
    keywords = [
        "museum", "gallery", "park", "garden",
        "temple", "church", "mosque",
        "university", "college", "school",
        "stadium", "swimming", "zoo",
        "attraction", "theatre", "cinema",
        "monument", "tower", "bridge",
        "palace", "square", "market",
    ]

    for keyword in keywords:
        try:
            attractions = search_osm_poi(keyword)

            for a in attractions:
                if a["name"] not in seen_names and is_chinese_name(a["name"]):
                    seen_names.add(a["name"])
                    all_attractions.append(a)

            time.sleep(1)  # 避免请求过快

        except Exception as e:
            print(f"  关键词'{keyword}'出错: {e}")
            continue

    print(f"\n总共获取到 {len(all_attractions)} 个景点")

    return all_attractions


def save_to_file(attractions, filename="attractions_beijing.json"):
    """
    保存数据到文件
    """
    import os
    data_dir = os.path.dirname(os.path.abspath(__file__))
    filepath = os.path.join(data_dir, filename)

    with open(filepath, 'w', encoding='utf-8') as f:
        json.dump({"attractions": attractions}, f, ensure_ascii=False, indent=2)

    print(f"数据已保存到: {filepath}")


