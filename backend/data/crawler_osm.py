# ==================== 配置 ====================

# Overpass API endpoint
OVERPASS_URL = "https://overpass-api.de/api/interpreter"

# 北京市边界（粗略矩形框）
BEIJING_BBOX = (39.4, 115.7, 41.1, 117.6)  # (south, west, north, east)

# ==================== Overpass QL查询 ====================

def build_overpass_query(keyword, bbox):
    """
    构建Overpass QL查询

    参数:
        keyword: 搜索关键词（英文）
        bbox: 边界框 (south, west, north, east)
    """
    south, west, north, east = bbox

    # Overpass QL 查询语法
    query = f"""
    [out:json][timeout:60];
    (
      node["name"~"{keyword}",bbox={south},{west},{north},{east}];
      way["name"~"{keyword}",bbox={south},{west},{north},{east}];
    );
    out center;
    """
    return query


